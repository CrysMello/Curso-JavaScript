// expressões resulte em True
let verdadeiro1 = 3 + 4 >= 5 && 9 >= 7;
let verdadeiro2 = 10 / 2 == 5 || 3 <= 2;
let verdadeiro3 = 7 - 5 !== 3 != 3 !== 2; 
console.log(verdadeiro1);
console.log(verdadeiro2);
console.log(verdadeiro3);

// expressões resulte em False

let false1 = 3 * 5 == 10 || 3 != 3;
let false2 = 4 - 3 >= 2 || 7 !== 7;
let false3 = 6 / 3 <= 2 && 3 >= 7;
console.log(false1);
console.log(false2);
console.log(false3);

